	<h3>Особенности и внутреннее устройство</h3>
	<p class="intro">
	Богатство фракции не знало пределов, о нем слагали легенды. Половину драгоценных металлов мафиози закопали на необитаемом острове в Тухлом океане, после чего появилось множество пиратов, искателей приключений, которые мечтают получить богатства мафиози. Вторую половину размели
	мафиози, не принявшие нацизм, которые разбрелись по всему земному шару. После прихода Рудольфа фракция сменилась с иерархичной на хаотичную.
