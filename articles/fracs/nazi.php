	<h3>Возникновение</h3>
	<p class="intro">Изначально фракция представляла собой узкий круг мафиози, что действовали в северной части Латышской Армении с 200-х
	гг до д.э. Занимались преимущественно традиционной для этих мест наркоторговлей и дискриминацией. Все было замечательно, пока во главу мафиози не встал повернутый на всю голову нацист Рудольф Литр. Прозвали его так благодаря тому, что каждое утро он выпивал литровую 
	бутылку ядреного шнапса. Неудивительно, что лет через 20 от организации не осталось и следа. Однажды во время путешествия в Южный Хоббитистан Рудольф отравился паленым шнапсом и умер от анальной горячки. Фракция осталась без лидера, что не мешало ей расширяться дальше.
	
