	<h3>Биография</h3>
	<p class="intro">Транс Евгений родился в маленьком уральском городке Мухосранске. В подростковом возрасте решил стать транси, после чего его стали гнобить. Женя решил(-а) свалить из Мухосранска. Побродив 8 лет по Еврейзии, он повстречал Абдуля, который и дал ему к 23 годам бессмертие. Вскоре он нашел единомышленников и стал сооснователем Рави Дару. Здесь он впервые почувствовал себя нужным, нашел сообщников и почувствовал себя по-настоящему счастливым, ведь таких верных друзей, как черные геи, он не встречал никогда в жизни. Спустя некоторое время он увлекся Абдуловерой, оказал огромную помощь <a href="./category.php?c=loc&p=kurti">Курцам</a>, чем заслужил гордое звание <a href="./category.php?c=fracs&p=adepts">Адепта</a>, но обыденная жизнь в Курцах ему надоела, поэтому он решил перебраться в Нигроморское, чтобы быть ближе к природе и морю. </p>
