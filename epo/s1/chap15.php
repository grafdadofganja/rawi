	<h2>Эпилог</h2>
	<p>
Много геев полегло,
Много крови утекло...
И разрозненные части
Победили разногласья.
Я вам более скажу:
Покурив в лесу ганжу,
Гей-маслята, что предали,
Все ошибки осознали!
Были ложны их мотивы,
Каждый, крикнув "Я скотина!",
Прочитав ,упавши, мантру,
Повернулся к лесу задом.
И Абдуль их всех простил,
Но работой загрузил!

Ну а кто стал вместо Майка?
Ты,читатель, угадай-ка!
Радостный сдержи свой стон,
Ведь у нас ВТОРОЙ СЕЗОН!
</p>
