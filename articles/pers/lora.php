	<h3>Биография</h3>
	<p class="intro">Лора- родилась в городке Залупівка на Днепре. В возрасте пяти лет малышку похитили османы и доставили в Саидовскую Арманию к Саиду. Там она стала его девочкой номер один и он рвал ей все каждый день по канонам ваххабитского ислама. В тринадцать лет, во время поездки по американским прериям, ей удалось убежать, став на 14 - летие королевой Дару. Ей принадлежит инициатива создания <a href="./category.php?c=fracs&p=gays">гей-армии</a>. После вступления в должность стала носить пояс целкомудрия. Героически погибла во время сражения под <a href="./category.php?c=loc&p=cerk">Черкасской деревушкой</a> во время группового изнасилования.</p>
